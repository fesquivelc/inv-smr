    private void tblFacturaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFacturaMouseReleased
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) {

            int fila = tblFactura.getSelectedRow();
            this.factura = lista.get(fila);

            numeroField.setText(factura.getNumeroFactura().toUpperCase());
//            fotoField.setText(BeanUtils.getProperty(bien, "foto"));

            ImageIcon fot = new ImageIcon(factura.getRuta());
            Icon icono = new ImageIcon(fot.getImage().getScaledInstance(lblFactura.getWidth(), lblFactura.getHeight(), Image.SCALE_DEFAULT));
            lblFactura.setIcon(icono);

        } else if (evt.getClickCount() == 2) {
            int fila = tblFactura.getSelectedRow();
            this.factura = lista.get(fila);
            this.dispose();
        }
    }//GEN-LAST:event_tblFacturaMouseReleased

