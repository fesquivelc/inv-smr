    private void listar() {
        lista = this.claseControlador.buscarTodos();
        lista = ObservableCollections.observableList(lista);

        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, lista, tblClase);

        BeanProperty bNombre = BeanProperty.create("codigo");
        BeanProperty bApellidoP = BeanProperty.create("nombre");

        binding.addColumnBinding(bNombre).setColumnName("CODIGO").setEditable(false);
        binding.addColumnBinding(bApellidoP).setColumnName("NOMBRE").setEditable(false);

        binding.bind();
    }

