    public void bindeoSalvaje(){
        lista = areaControlador.buscarTodos();
        lista = ObservableCollections.observableList(lista);
        
        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, lista, tabla);

        BeanProperty bId = BeanProperty.create("codigo");
        BeanProperty bNombre = BeanProperty.create("nombre");
        BeanProperty bArea = BeanProperty.create(BeanProperty.create("area"), "nombre");

        binding.addColumnBinding(bId).setColumnName("Código").setEditable(false).setColumnClass(String.class);
        binding.addColumnBinding(bNombre).setColumnName("Nombre").setEditable(false).setColumnClass(String.class);
        binding.addColumnBinding(bArea).setColumnName("Área").setEditable(false).setColumnClass(String.class);

        binding.bind();
    }

